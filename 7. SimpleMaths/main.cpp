#include <iostream>

using namespace std;

int main() {
    cout << "Addition" << endl;
    int x = x;
    cin >> x;
    int y = y;
    cin >> y;
    float z = float(x)+y;

    cout << x << " " << "+" << " " << y << " " << "=" << " " << z << endl;
    cout << endl;

    cout << "Multiplication" << endl;
    int u = u;
    cin >> u;
    int v = v;
    cin >> v;
    float w = float(u)*v;

    cout << u << " " << "*" << " " << v << " " << "=" << " " << w << endl;
    cout << endl;

    cout << "Division" << endl;
    int r = r;
    cin >> r;
    int s = s;
    cin >> s;
    float t = float(r)/s;

    cout << r << " " << "/" << " " << s << " " << "=" << " " << t << endl;
    cout << endl;

    cout << "Subtraction" << endl;
    int n = n;
    cin >> n;
    int p = p;
    cin >> p;
    float q = float(n)-p;

    cout << n << " " << "-" << " " << p << " " << "=" << " " << q << endl;
    cout << endl;

    cout << "Modulo" << endl; //Remainder Calculation
    int k = k;
    cin >> k;
    int l = l;
    cin >> l;
    float m = k%l;

    cout << k << " " << "%" << " " << l << " " << "=" << " " << m << endl;
    cout << endl;
}